	<h2>Biography</h2>

	<p>Benjamin Shepherd began playing the piano aged 14. Within a year, Benjamin was offered a place at the 
	<a href="http://www.rwcmd.ac.uk/">Royal Welsh College of Music and Drama</a>, in its junior department, 
	studying with Penny Davies where he won the 'Beattie' prize for piano.</p>

	<p>Benjamin went on to study on the Bachelor of Music Degree (Honours) Programme at <a href="http://www.trinitylaban.ac.uk/">
	Trinity College of Music</a>, London, with <a href="http://www.tcm.ac.uk/RVEc5ce3d79d3c54045ad0ec327ca477646,,.aspx">
	Alexander Ardakov</a>. After two successful auditions, Benjamin was selected to study his third year 
	abroad, on the Erasmus Programme, at the <a href="http://www.vsmu.sk/">Academy of Music and Dramatic Arts</a> 
	in Bratislava, Slovak Republic, with Professor <a href="http://www.pozon.sk/lapsansky/index.html">Marián Lapšanský</a>
	 (Director of the <a href="http://www.filharm.sk/">Slovak Philharmonic</a>). 
	Whilst abroad, he set up links with the Slovak Composer, <a href="http://www.hc.sk/src/osobnost.php?lg=en&oid=360">
	Juraj Hatrík</a> and has promoted his music in the UK. After returning to the UK and completing his Bachelor Degree with
	<a href="http://www.philipfowke.co.uk">Philip Fowke</a> (Senior Fellow), 
	Benjamin then progressed as a Scholar (Rosamund Mott and Alfred Kitchin Scholarships) to the Master of Music Programme, which he completed 
	in 2010.</p>

	<p>Benjamin enjoys regular performances, and has performed throughout the UK and abroad. Recitals have included 
	engagements at Steinway Hall, Cheltenham Pump Rooms, Blackheath Halls, Reading Minster and for various Music Societies. He has performed at various charity concerts 
	and worked with numerous musicians with notable acclaim. Benjamin actively participates in piano masterclasses, and has benefited from expert guidance from
  musicians such as Andrei Gavrilov and Alberto Portugheis. As well as solo piano music, Benjamin also enjoys performing 
	a wide range of chamber and vocal music.</p>
	
